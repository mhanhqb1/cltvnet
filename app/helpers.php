function getImageUrl($image)
{
    if (strpos($image, 'http') !== false) {
        $imageUrl = $image;
    } else {
        $imageUrl = url('/storage/' . $image);
    }
    return $imageUrl;
}

function getVideoNameFromDailyPlayList($name, $isSeries = 1)
{
    $txt = 1;
    if ($isSeries == 1) {
        $name = explode(' - ', $name);
        if (!empty($name[1])) {
            $txt = 'Capítulo ' . $name[1];
        }
    }
    return $txt;
}

function getNumber($str)
{
    return preg_replace('/[^0-9]/', '', $str);
}

function getStringBetweenTwoStrings($str, $start, $end)
{
    $result = '';
    if (preg_match('/' . $start . '(.*?)' . $end . '/', $str, $match) == 1) {
        $result = $match[1];
    }
    return $result;
}

